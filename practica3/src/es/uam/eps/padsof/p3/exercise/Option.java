package es.uam.eps.padsof.p3.exercise;

public class Option {
	private String option;

	/**
	 * @param option
	 */
	public Option(String option) {
		super();
		this.option = option;
	}

	/**
	 * @return the option
	 */
	public String getOption() {
		return option;
	}

	/**
	 * @param option the option to set
	 */
	public void setOption(String option) {
		this.option = option;
	}
	
	
}
